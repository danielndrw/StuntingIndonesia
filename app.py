@st.cache_data
def load_geojson():
    # URL aktif per 2025-10; gunakan raw file provinsi
    url = "https://raw.githubusercontent.com/superpikar/indonesia-geojson/master/indonesia-prov.geojson"
    try:
        r = requests.get(url, timeout=10)
        r.raise_for_status()
        st.success("ğŸŒ GeoJSON berhasil dimuat dari sumber online.")
        return r.json()
    except Exception as e:
        st.warning(f"âš ï¸ Gagal memuat GeoJSON online: {e}")

    # fallback lokal
    local_path = "indonesia_province.geojson"
    if os.path.exists(local_path):
        try:
            with open(local_path, "r", encoding="utf-8") as f:
                st.info("ğŸ“ Menggunakan GeoJSON lokal sebagai cadangan.")
                return json.load(f)
        except Exception as e:
            st.warning(f"âš ï¸ GeoJSON lokal bermasalah: {e}")

    # fallback dummy titik (tetap bisa divisualisasikan sebagai scatter_geo jika perlu)
    st.error("âŒ Tidak ada GeoJSON valid. Menggunakan koleksi titik koordinat manual.")
    dummy = {"type": "FeatureCollection", "features": []}
    for prov, (lat, lon) in PROV_COORDS.items():
        dummy["features"].append({
            "type": "Feature",
            "properties": {"Propinsi": prov},
            "geometry": {"type": "Point", "coordinates": [lon, lat]},
        })
    return dummy

